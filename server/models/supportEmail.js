import mongoose from 'mongoose';

const { Schema } = mongoose;

const SupportEmailSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    subject: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model('SupportEmail', SupportEmailSchema);
